                <h3 class="mt-4 mt-xl-5 text-dark">What Are Invisalign Attachments?</h3>
                <p>
                    Attachments are small, tooth-colored dots made of dental composite that are temporarily bonded to certain teeth during Invisalign treatment. They act as anchors, providing additional grip for the aligners.
                </p>

                <h3 class="mt-4 mt-xl-5 text-dark">Function and Purpose</h3>
                <p>
                    Their primary role is to facilitate more complex tooth movements, which might not be possible with aligners alone. For instance, rotating a tooth or moving it vertically requires more force, which these attachments provide.
                </p>

